package ua.javarush.java.core.level25.task09;

public class Solution {
    public static void main(String[] args) {
        // Створюємо два об’єкти SystemCore, щоб побачити порядок ініціалізації.
        // Під час першого створення класу спочатку виконається статичний блок (один раз за всю програму)
        new SystemCore();

        // Під час другого створення виконається лише нестатичний блок (статичний уже відпрацював).
        new SystemCore();
    }
}

// Клас «складної системи», у якому демонструється порядок ініціалізації
class SystemCore {
    // Статичний блок: виконується один раз під час першого завантаження класу в пам’ять JVM
    static {
        System.out.println("Система: глобальну ініціалізацію завершено.");
    }

    // Нестатичний (ініціалізаційний) блок: виконується під час кожного створення об’єкта
    {
        System.out.println("Компонент: індивідуальне налаштування завершено.");
    }
}